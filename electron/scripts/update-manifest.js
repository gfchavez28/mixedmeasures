// Auto-update manifest surgery for the release pipeline (#29 slab S1).
//
// electron-builder generates latest-mac.yml at package time, which is wrong in
// two ways for THIS pipeline:
//
//   1. STALE HASH — the mac job notarizes + staples the .dmg AFTER packaging
//      (electron-builder staples the .app but not the .dmg container; the rc2
//      finding), so the dmg bytes change after its manifest entry was computed.
//      `patch` recomputes sha512/size for every listed artifact found on disk.
//
//   2. ARCH CLOBBER — the two mac legs (arm64 on macos-14, x64 on macos-13)
//      each generate a latest-mac.yml listing ONLY their own arch's files and
//      upload to the same draft release: last writer wins and the other arch's
//      users would hash-mismatch on every update. `merge` combines the per-arch
//      manifests into one (electron-updater picks its file by arch suffix).
//
// Deliberately dependency-free (node stdlib only) and line-based: the input is
// machine-generated by electron-builder with a fixed shape — parse THAT shape
// strictly and fail loudly on anything unexpected, rather than silently
// producing a manifest the updater will trust.
//
// CLI:
//   node update-manifest.js patch <manifest.yml> <artifact-dir>
//   node update-manifest.js merge <out.yml> <in1.yml> [in2.yml ...]
//
// Unit tests: update-manifest.test.js (runs in the electron `node --test` suite).

'use strict'

const crypto = require('crypto')
const fs = require('fs')
const path = require('path')

/** Parse an electron-builder latest*.yml into {version, entries, tail, head}.
 *
 * entries = [{url, lines: [raw sub-lines]}] preserving unknown keys
 * (blockMapSize etc.) verbatim. head = lines before `files:`; tail = lines
 * after the last files entry (path/sha512/releaseDate block).
 */
function parseManifest(text) {
  const lines = text.split('\n')
  const head = []
  const entries = []
  const tail = []
  let version = null
  let state = 'head' // head → files → tail
  let current = null

  for (const line of lines) {
    const versionMatch = line.match(/^version:\s*(.+)\s*$/)
    if (versionMatch && state === 'head') version = versionMatch[1]

    if (state === 'head') {
      head.push(line)
      if (/^files:\s*$/.test(line)) state = 'files'
      continue
    }

    if (state === 'files') {
      const urlMatch = line.match(/^ {2}- url:\s*(.+)\s*$/)
      if (urlMatch) {
        current = { url: urlMatch[1], lines: [] }
        entries.push(current)
        continue
      }
      if (/^ {4}\S/.test(line)) {
        if (!current) throw new Error(`manifest sub-line before any files entry: ${line}`)
        current.lines.push(line)
        continue
      }
      // First non-indented line ends the files block.
      state = 'tail'
      tail.push(line)
      continue
    }

    tail.push(line)
  }

  if (version === null) throw new Error('manifest has no version: line')
  if (state === 'head') throw new Error('manifest has no files: block')
  return { version, head, entries, tail }
}

function renderManifest(m) {
  const out = [...m.head]
  for (const e of m.entries) {
    out.push(`  - url: ${e.url}`)
    out.push(...e.lines)
  }
  out.push(...m.tail)
  return out.join('\n')
}

function sha512Base64(filePath) {
  const hash = crypto.createHash('sha512')
  hash.update(fs.readFileSync(filePath))
  return hash.digest('base64')
}

/** Rewrite an entry's sub-line value, asserting the key exists (electron-builder
 * always emits sha512 + size — a missing key means the format shifted, and a
 * silently un-patched manifest is exactly the bug this tool exists to prevent). */
function setEntryValue(entry, key, value) {
  const idx = entry.lines.findIndex((l) => l.match(new RegExp(`^ {4}${key}:`)))
  if (idx === -1) throw new Error(`entry ${entry.url} has no ${key}: line`)
  entry.lines[idx] = `    ${key}: ${value}`
}

/** Recompute sha512/size for every files entry present in artifactDir; also
 * fix the top-level sha512 when the top-level path points at a patched file.
 * Returns the urls actually patched (for CI logs). */
function patchManifest(manifestText, artifactDir) {
  const m = parseManifest(manifestText)
  const patched = []
  const hashByUrl = new Map()

  for (const entry of m.entries) {
    const file = path.join(artifactDir, entry.url)
    if (!fs.existsSync(file)) continue
    const sha = sha512Base64(file)
    const size = fs.statSync(file).size
    setEntryValue(entry, 'sha512', sha)
    setEntryValue(entry, 'size', String(size))
    hashByUrl.set(entry.url, sha)
    patched.push(entry.url)
  }

  if (patched.length === 0) {
    throw new Error(`no manifest artifacts found in ${artifactDir} — wrong dir?`)
  }

  // Top-level legacy fields: path + sha512 mirror the first file.
  const pathIdx = m.tail.findIndex((l) => l.match(/^path:\s*(.+)$/))
  if (pathIdx !== -1) {
    const topPath = m.tail[pathIdx].match(/^path:\s*(.+)\s*$/)[1]
    if (hashByUrl.has(topPath)) {
      const shaIdx = m.tail.findIndex((l) => l.match(/^sha512:\s*/))
      if (shaIdx === -1) throw new Error('manifest has top-level path but no top-level sha512')
      m.tail[shaIdx] = `sha512: ${hashByUrl.get(topPath)}`
    }
  }

  return { text: renderManifest(m), patched }
}

/** Merge per-arch manifests into one: first manifest's head/tail win, later
 * manifests contribute their files entries (deduped by url). All inputs must
 * agree on version — mixing releases would hand the updater a lying manifest. */
function mergeManifests(texts) {
  if (texts.length === 0) throw new Error('nothing to merge')
  const parsed = texts.map(parseManifest)
  const base = parsed[0]
  const seen = new Set(base.entries.map((e) => e.url))

  for (const m of parsed.slice(1)) {
    if (m.version !== base.version) {
      throw new Error(`version mismatch across manifests: ${base.version} vs ${m.version}`)
    }
    for (const entry of m.entries) {
      if (seen.has(entry.url)) continue
      seen.add(entry.url)
      base.entries.push(entry)
    }
  }

  return { text: renderManifest(base), urls: base.entries.map((e) => e.url) }
}

function main(argv) {
  const [mode, ...args] = argv
  if (mode === 'patch') {
    const [manifestPath, artifactDir] = args
    if (!manifestPath || !artifactDir) {
      throw new Error('usage: update-manifest.js patch <manifest.yml> <artifact-dir>')
    }
    const { text, patched } = patchManifest(fs.readFileSync(manifestPath, 'utf8'), artifactDir)
    fs.writeFileSync(manifestPath, text)
    console.log(`patched ${patched.length} entr${patched.length === 1 ? 'y' : 'ies'} in ${manifestPath}:`)
    for (const url of patched) console.log(`  ${url}`)
    return
  }
  if (mode === 'merge') {
    const [outPath, ...inputs] = args
    if (!outPath || inputs.length === 0) {
      throw new Error('usage: update-manifest.js merge <out.yml> <in1.yml> [in2.yml ...]')
    }
    const { text, urls } = mergeManifests(inputs.map((p) => fs.readFileSync(p, 'utf8')))
    fs.writeFileSync(outPath, text)
    console.log(`merged ${inputs.length} manifest(s) → ${outPath} (${urls.length} files):`)
    for (const url of urls) console.log(`  ${url}`)
    return
  }
  throw new Error(`unknown mode: ${mode} (expected patch|merge)`)
}

if (require.main === module) {
  try {
    main(process.argv.slice(2))
  } catch (err) {
    console.error(`update-manifest: ${err.message}`)
    process.exit(1)
  }
}

module.exports = { parseManifest, renderManifest, patchManifest, mergeManifests, sha512Base64 }
